package com.jeugene.retrex;

public class fin {

    private String Head;
    private String weburl;

    public fin(String head, String weburl) {
        Head = head;
        this.weburl = weburl;
    }

    public String getHead() {
        return Head;
    }

    public void setHead(String head) {
        Head = head;
    }

    public String getWeburl() {
        return weburl;
    }

    public void setWeburl(String weburl) {
        this.weburl = weburl;
    }
}



